/** Copy text to the system clipboard.
 *
 * Uses the async Clipboard API when permitted; falls back to a transient
 * textarea + execCommand for older browsers or when Permissions-Policy blocks
 * navigator.clipboard (see frontend/static/.ic-assets.json5).
 */
export async function copyText(text: string): Promise<boolean> {
  if (typeof navigator !== 'undefined' && navigator.clipboard?.writeText) {
    try {
      await navigator.clipboard.writeText(text);
      return true;
    } catch {
      /* Permissions-Policy or non-secure context — try fallback */
    }
  }

  try {
    const ta = document.createElement('textarea');
    ta.value = text;
    ta.setAttribute('readonly', '');
    ta.style.position = 'fixed';
    ta.style.left = '-9999px';
    document.body.appendChild(ta);
    ta.select();
    const ok = document.execCommand('copy');
    document.body.removeChild(ta);
    return ok;
  } catch {
    return false;
  }
}
